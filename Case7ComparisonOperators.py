a = 5

b = 2

# equal to operator
print(' a == b =', a == b) #answer will be bool 2 int variable are compared

# not equal to operator
print('a != b =', a != b) #same answer bool

# greater than operator
print('a > b =', a > b) #same answer bool

# less than operator
print('a < b =', a < b) #same answer bool

# greater than or equal to operator
print('a >= b =', a >= b) 

# less than or equal to operator
print('a <= b =', a <= b)